/** Look up a single definition by slug — throws if not found. */
export function getAchievementBySlug(slug: string): AchievementDefinition {
  const def = ACHIEVEMENT_DEFINITIONS.find((d) => d.slug === slug)
  if (!def) throw new Error(`Achievement definition not found: ${slug}`)
  return def
}

/** Filter definitions by pillar. */
export function getAchievementsByPillar(pillar: AchievementPillar): AchievementDefinition[] {
  return ACHIEVEMENT_DEFINITIONS.filter((d) => d.pillar === pillar)
}
