/**
 * Achievement & Index System — Domain Definitions
 *
 * 50 achievements across 4 pillars: FAN, BAND, DJ, LABEL.
 * Each definition is the single source of truth consumed by:
 *   - The database seed (achievement_definitions table)
 *   - The granting engine (achievementEngine.ts)
 *   - The UI badge renderer (AchievementBadge component)
 *
 * Rarity tiers:
 *   COMMON    — achievable by most active users
 *   RARE      — requires sustained effort or specific conditions
 *   EPIC      — top-tier performance or long-term dedication
 *   LEGENDARY — near-impossible; true scene status markers
 */

export type AchievementPillar = 'FAN' | 'BAND' | 'DJ' | 'LABEL'
export type AchievementRarity = 'COMMON' | 'RARE' | 'EPIC' | 'LEGENDARY'

export interface AchievementDefinition {
  /** Stable identifier — never rename after release. */
  slug: string
  pillar: AchievementPillar
  rarity: AchievementRarity
  /** lucide-react icon name. */
  iconKey: string
  titleDe: string
  titleEn: string
  descDe: string
  descEn: string
  /** If true, KYC-approved status required before granting. */
  requiresKyc: boolean
  /** If true, granted automatically by the cron engine (not manually). */
  isAutomatic: boolean
}

